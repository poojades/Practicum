    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        //perform validations
        if (jTextField1.getText() == null || jTextField1.getText().equals("")) {
            jLabel6.setText("Please enter username");
            jLabel6.setVisible(true);
        } else if (jTextField2.getText() == null || jTextField2.getText().equals("")) {
            jLabel6.setText("Please select key provided by Amazon");
            jLabel6.setVisible(true);
        } else if (jTextField3.getText() == null || jTextField3.getText().equals("")) {
            jLabel6.setText("Please enter Access key provided by Amazon");
            jLabel6.setVisible(true);
        } else if (jTextField4.getText() == null || jTextField4.getText().equals("")) {
            jLabel6.setText("Please select Secret key provided by Amazon");
            jLabel6.setVisible(true);
        } else {
           
            String userName = jTextField1.getText();
            String pemFilePath = jTextField2.getText();
            System.out.println(pemFilePath
                    );
           // String accessKey = jTextField3.getText();
            //String secretKey = jTextField4.getText();
            String accessKey="AKIAIHLBFFYWOAIOXLPQ";
               String secretKey = "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";
          
            int flag = ConnectToAmazonEC2.connectToCloud(pemFilePath, accessKey, secretKey);

            if (flag != 0) {
                jLabel6.setText("Oops, failed to connect to Amazon EC2. Please verify your credentials");
                jLabel6.setVisible(true);
            } else {
                //Open the Main menu for Uploading - downloading - retrieving the file
                MainMenu newmenu = new MainMenu(userName,pemFilePath);
                newmenu.setVisible(true);
                setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

